# Step 1: Get the score input from the user
score = float(input("Enter the student's score: "))

# Step 2: Check if the score is greater than or equal to 35
if score >= 35:
    # Step 3: Print "Pass" if the score is 35 or above
    print("Result: Pass")
else:
    # Step 3: Print "Fail" if the score is below 35
    print("Result: Fail")



# Step 1: Get the number input from the user
number = int(input("Enter an integer: "))

# Step 2: Check if the number is even or odd
if number % 2 == 0:
    # Step 3: Print "Even" if the number is divisible by 2
    print("Result: Even")
else:
    # Step 3: Print "Odd" if the number is not divisible by 2
    print("Result: Odd")




